package com.maoyan.bean;

/**
 * 电影院图片信息
 * @author 振
 *
 */
public class CinemaPic {
	//电影院图片id
	private int cinemaPicId;
	//电影院图片
	private String cinemaPic;
	//电影院图片
	private String cinemaName;
	
	public int getCinemaPicId() {
		return cinemaPicId;
	}
	public void setCinemaPicId(int cinemaPicId) {
		this.cinemaPicId = cinemaPicId;
	}
	public String getCinemaPic() {
		return cinemaPic;
	}
	public void setCinemaPic(String cinemaPic) {
		this.cinemaPic = cinemaPic;
	}
	public String getCinemaName() {
		return cinemaName;
	}
	public void setCinemaName(String cinemaName) {
		this.cinemaName = cinemaName;
	}
	
	
	
}
